def day7_part2():
    best_score = (-1, (-1))
    low_phase_setting = 5
    amp_count = 5
    for perm in itertools.permutations(range(low_phase_setting,low_phase_setting + amp_count)):
        qs = []
        for i, p in enumerate(perm):
            q = queue.Queue()
            q.put(p)
            if i == 0:
                q.put(0)
            qs.append(q)
        with ThreadPoolExecutor(max_workers = amp_count) as executor:
            for inq, outq in zip(qs, qs[1:] + [qs[0]]):
                executor.submit(get_day7_program().run, inq, outq)
        score = (qs[0].queue.pop(), perm)
        if score > best_score:
            best_score = score
    return best_score


